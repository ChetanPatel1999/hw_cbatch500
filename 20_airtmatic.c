// airthmatic operator example
#include <stdio.h>
void main()
{
    int a, b, c;
    a = 15;
    b = 4;
    c = a + b;
    printf("addition =%d\n", c); // 19
    c = a - b;
    printf("subtraction =%d\n", c); // 11
    c = a * b;
    printf("multiplication =%d\n", c); // 60
    c = a / b;
    printf("division =%d\n", c); // 3
    c = a % b;
    printf("modules/reminder =%d\n", c); // 3
}