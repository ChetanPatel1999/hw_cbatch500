// airthmatic operator example
#include <stdio.h>
void main()
{
    int a, b, c;
    a = 20;
    b = 4;
    c = a + b;
    printf("addition =%d\n", c); // 24
    c = a - b;
    printf("subtraction =%d\n", c); // 16
    c = a * b;
    printf("multiplication =%d\n", c); // 80
    c = a / b;
    printf("division =%d\n", c); // 5
    c = a % b;
    printf("modules/reminder =%d\n", c); // 0
}